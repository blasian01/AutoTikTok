openai.api_key = "OpenAI-API"

def generate_anime_prompt(random_anime_theme):
    prompt = f"""Generate a 900-second deform prompt for a cool anime edit featuring the theme {random_anime_theme}. make the format look like this. {{
        "0": "{random_anime_theme} characters in school, 'New Day' text overlay",
        "50": "fighting scene, 'Epic Battle' text",
        "100": "romantic moment, 'Love Blooms' text",
        "150": "characters eating together, 'Friendship' text",
        "200": "suspenseful scene, 'Twist in Plot' text",
        "250": "crying scene, 'Heartbreaking' text",
        "300": "villain's entrance, 'New Threat' text",
        "350": "cliffhanger, 'To be Continued' text",
        "400": "epic transformations, 'Level Up' text",
        "450": "rescue mission, 'Heroes Assemble' text",
        "500": "flashback scene, 'Nostalgia' text",
        "550": "training montage, 'Getting Stronger' text",
        "600": "plot revelation, 'Hidden Secrets' text",
        "650": "comedic relief, 'Light Moment' text",
        "700": "side characters spotlight, 'Unsung Heroes' text",
        "750": "ultimate move revealed, 'Secret Technique' text",
        "800": "resolution of conflicts, 'End of Battles' text",
        "850": "farewell, 'Goodbyes' text",
        "900": "final scene with preview for next season, 'Until Next Time' text"
    }}"""

    response = openai.Completion.create(
        engine="text-davinci-002",
        prompt=prompt,
        max_tokens=500
    )
    return response.choices[0].text.strip()

